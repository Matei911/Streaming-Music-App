public class Streams {
    private Integer id;
    private String name;
    private Integer streamType;
    private Integer streameGenre;
    private Long noOfStreams;
    private Integer streamerID;
    private Long dateAdded;
    private Long length;


    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getStreamType() {
        return streamType;
    }

    public void setStreamType(Integer streamType) {
        this.streamType = streamType;
    }

    public Integer getStreameGenre() {
        return streameGenre;
    }

    public void setStreameGenre(Integer streameGenre) {
        this.streameGenre = streameGenre;
    }

    public Long getNoOfStreams() {
        return noOfStreams;
    }

    public void setNoOfStreams(Long noOfStreams) {
        this.noOfStreams = noOfStreams;
    }

    public Integer getStreamerID() {
        return streamerID;
    }

    public void setStreamerID(Integer streamerID) {
        this.streamerID = streamerID;
    }

    public Long getDateAdded() {
        return dateAdded;
    }

    public void setDateAdded(Long dateAdded) {
        this.dateAdded = dateAdded;
    }

    public Long getLength() {
        return length;
    }

    public void setLength(Long length) {
        this.length = length;
    }
}
